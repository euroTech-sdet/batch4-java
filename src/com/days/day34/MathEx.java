package com.days.day34;

public class MathEx {

    public static void main(String[] args) {

        System.out.println(Math.max(2, 1));
        System.out.println("Math.PI = " + Math.PI);

        System.out.println("Math.ceil(2.1) = " + Math.ceil(2.1));
        System.out.println("Math.floor(2.9) = " + Math.floor(2.9));
        System.out.println("Math.round(2.51) = " + Math.round(2.51)); // returns LONG!!
        System.out.println("Math.round(2.1f) = " + Math.round(2.1f)); // returns INT!!!!!!!!!!!

    }
}
