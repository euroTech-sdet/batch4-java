package com.days.day6;

public class Operators {
    public static void main(String[] args) {
        System.out.println(100 + 20);
        System.out.println(2100 + 270);
        System.out.println(1500 + 9820);
        int num;
        num = 500;
        int num2 = num;
        System.out.println("num2 = " + num2);
        System.out.println(num + num2);
        int sum = num + num2;
        System.out.println("sum = " + sum);
        int no = 900;
        int sum2 = num + no;// ctrl +alt +l   formats the page
        System.out.println("sum2 = " + sum2);
        System.out.println(sum2);
        System.out.println(900 - 200);
        System.out.println(sum2 - sum);
        int result = sum2 - sum;
        System.out.println("result = " + result);
        System.out.println(sum - num);

        System.out.println(100 / 5);
        int five = 1000 / 5;
        System.out.println("five = " + five);

        System.out.println(100 / 3);//  33*3=99    100-99=  1
        System.out.println(100 % 3);// 1

        System.out.println(100 / 6);// 16*6=96   100-96=4
        System.out.println(100 % 6);// 4

        int six = 100 % 6;

        System.out.println("six = " + six);

        int seven = 100 % 7;// alt +  enter  assigns value to local variable
        System.out.println("seven = " + seven);

        System.out.println(100 / 6);
        System.out.println(100 / 3);
        System.out.println(33 * 3);
        double sevenD = 100.0 / 7;
        System.out.println("sevenD = " + sevenD);
        double eight = 100.0 / 8;
        System.out.println("eight = " + eight);

        System.out.println(100 / 8.0);// if you want to exact result one of the operand must be decimal
        System.out.println(100 / 7.0);//  ctrl + d   repeat the line
        System.out.println(100 / 7);//  ctrl + d   repeat the line




    }
}
